package com.nt.vo;

public class LaptopVO {
	private String lapno;
	private String lapname;
	private String laprice;
	private String laplength;
	private String lapweight;
	public String getLapno() {
		return lapno;
	}
	public void setLapno(String lapno) {
		this.lapno = lapno;
	}
	public String getLapname() {
		return lapname;
	}
	public void setLapname(String lapname) {
		this.lapname = lapname;
	}
	public String getLaprice() {
		return laprice;
	}
	public void setLaprice(String laprice) {
		this.laprice = laprice;
	}
	public String getLaplength() {
		return laplength;
	}
	public void setLaplength(String laplength) {
		this.laplength = laplength;
	}
	public String getLapweight() {
		return lapweight;
	}
	public void setLapweight(String lapweight) {
		this.lapweight = lapweight;
	}
	
	
}
