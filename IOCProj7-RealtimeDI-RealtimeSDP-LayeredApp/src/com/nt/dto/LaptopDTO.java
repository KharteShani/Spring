package com.nt.dto;

public class LaptopDTO {
	private int lapno;
	private String lapname;
	private float lapprice;
	private float laplength;
	private float lapweight;
	public int getLapno() {
		return lapno;
	}
	public void setLapno(int lapno) {
		this.lapno = lapno;
	}
	public String getLapname() {
		return lapname;
	}
	public void setLapname(String lapname) {
		this.lapname = lapname;
	}
	public float getLapprice() {
		return lapprice;
	}
	public void setLapprice(float lapprice) {
		this.lapprice = lapprice;
	}
	public float getLaplength() {
		return laplength;
	}
	public void setLaplength(float laplength) {
		this.laplength = laplength;
	}
	public float getLapweight() {
		return lapweight;
	}
	public void setLapweight(float lapweight) {
		this.lapweight = lapweight;
	}
		
}
